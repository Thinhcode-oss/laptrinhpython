--CAU100
--i
CREATE OR ALTER VIEW v_Cau100A
AS
SELECT DTB
FROM SinhVien
WHERE MaKhoa = 'CNTT';

--ii
CREATE OR ALTER VIEW v_Cau100B
AS
SELECT MSSV, Ten, MaKhoa, DTB
FROM SinhVien
WHERE MaKhoa <> 'CNTT';

--iii
CREATE OR ALTER VIEW v_Cau100C
SELECT *
FROM v_Cau100B
WHERE DTB > (
    SELECT MIN(DTB)
    FROM v_Cau100A
);
